CREATE TABLE OMDB (
title varchar(255),
year varchar(255),
rated varchar(255),
released varchar(255),
runtime varchar(255),
genre varchar(255),
director varchar(255),
writer varchar(255),
actors varchar(255),
plot varchar(255),
language varchar(255),
country varchar(255),
awards varchar(255),
poster varchar(255),
metascore varchar(255),
imdbRating varchar(255),
imdbVotes varchar(255),
imdbID varchar(255) PRIMARY KEY,
type varchar(255)
);